1. Quali sono i voli di durata maggiore della durata media di tutti i voli della stessa
compagnia? Restituire il codice del volo, la compagnia e la durata.
--query1
with DurataTotaleVoli as (
    select comp ,avg(durataMinuti) as dur_tot
    from Volo 
    group by comp 
)
select  distinct v.codice, v.comp , v.durataMinuti 
from Volo as v , DurataTotaleVoli as dv
group by v.codice , v.comp , dv.dur_tot
having dv.dur_tot < v.durataMinuti
